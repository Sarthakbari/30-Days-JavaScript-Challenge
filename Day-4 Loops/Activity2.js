// Task 3:- WAP to calculate the sum of number from 1 to 10 using a while loop.
let a = 1;
let sum = 0;
while (a <= 10) {
    sum += a ; 
    a++ ;
}
console.log(sum);

// Task 4:- WAP to print a number from 10 to 1 using a while loop.
let i = 10;
while (i >= 1) {
    console.log(i);
    i-- ;   
}
